#include <iostream>
#include <cmath>
using namespace std;
int main()
{
	cout << "abs(8) = " << abs(8) << endl;
	cout << "abs(-8) = " << abs(-8) << endl << endl;

	cout << "floor(12.78) = " << floor(12.78) << endl;
	cout << "ceil(12.78) = " << ceil(12.78) << endl << endl;

	cout << "log(100) = " << log(100) << endl;
	cout << "log10(100) = " << log10(100) << endl << endl;

	cout << "exp(5) = " << exp(5) << endl;
	cout << "pow(2, 3) = " << pow(2, 3) << endl << endl;

	cout << "sqrt(100) = " << sqrt(100) << endl;

	return 0;
}